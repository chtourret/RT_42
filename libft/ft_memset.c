#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	unsigned char	*p;

	p = (unsigned char *)b;
	while (n > 0)
	{
		*p = (unsigned char)c;
		n--;
		p++;
	}
	return (b);
}
