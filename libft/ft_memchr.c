#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *src;

	src = (unsigned char*)s;
	while (n > 0 && *src != (unsigned char)c)
	{
		src++;
		n--;
	}
	if (n == 0)
		return (NULL);
	else
		return ((void*)src);
}
