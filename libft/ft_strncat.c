#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *dest;
	char *src;

	dest = s1;
	src = (char*)s2;
	if (!*src)
		return (s1);
	while (*dest)
		dest++;
	while (*src && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	*dest = '\0';
	return (s1);
}
