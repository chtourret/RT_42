#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char *source;
	char *desti;

	source = (char *)src;
	desti = (char *)dst;
	while (n > 0)
	{
		*desti = *source;
		desti++;
		source++;
		n--;
	}
	return (dst);
}
